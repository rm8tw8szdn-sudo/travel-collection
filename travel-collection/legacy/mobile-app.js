function formatDateRange(start, end) {
  if (!start && !end) return "时间待定";
  const normalize = (value) => value ? value.replaceAll("-", ".") : "待定";
  return `${normalize(start)} - ${normalize(end)}`;
}

function tripDays(trip) {
  if (!trip.startDate || !trip.endDate) return "";
  const start = new Date(`${trip.startDate}T00:00:00+08:00`);
  const end = new Date(`${trip.endDate}T00:00:00+08:00`);
  const days = Math.max(1, Math.round((end - start) / 86400000) + 1);
  return `${days} 天`;
}

function daysUntilTrip(trip) {
  if (!trip.startDate) return "";
  const start = new Date(`${trip.startDate}T00:00:00+08:00`);
  const days = Math.ceil((start - CURRENT_DATE) / 86400000);
  if (days < 0) return "";
  if (days === 0) return "今天出发";
  return `还有 ${days} 天`;
}

