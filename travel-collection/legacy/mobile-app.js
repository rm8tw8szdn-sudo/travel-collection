function groupIndex(place) {
  if (state.mode === "world") {
    const routeIndex = travelGroups.findIndex((group) => group.codes.includes(place.code));
    if (routeIndex !== -1) return routeIndex;
  }
  if (state.mode === "china") return provinceRank(place.code);
  const groups = geoGroupOrder[state.mode];
  const index = groups.indexOf(place.group);
  return index === -1 ? groups.length : index;
}

function visiblePlaces() {
  const query = state.search.trim().toLowerCase();
  return currentPlaces()
    .map((place) => ({ ...place, status: getStatus(place.code) }))
    .filter((place) => {
      if (state.mode === "china" && state.filter !== "all") {
        const cities = getProvinceCities(place);
        if (state.filter === "visited") return cities.some((city) => cityStatusSets.china.visited.has(cityCode(city)));
        if (state.filter === "blank") return cities.some((city) => !cityStatusSets.china.visited.has(cityCode(city)));
        if (state.filter === "upcoming") return cities.some((city) => state.upcomingCities.has(cityCode(city)));
      }
      if (state.filter === "all") return true;
      if (state.filter === "upcoming") return state.mode === "world" && state.upcomingCountries.has(place.code);
      if (state.filter === "visited" && query && state.mode === "world") return true;
      return place.status === state.filter;
    })
    .filter((place) => {
      if (!query) return true;
      if (place.name.toLowerCase().includes(query) || place.code.toLowerCase().includes(query)) return true;
      if (state.mode !== "china") return false;
      return getProvinceCities(place).some((city) => city.name.toLowerCase().includes(query));
    })
    .sort((a, b) => {
      const statusOrder = { visited: 0, blank: 1 };
      if (state.sortVisitedFirst) {
        return statusOrder[a.status] - statusOrder[b.status] || groupIndex(a) - groupIndex(b) || a.name.localeCompare(b.name, "zh-CN");
      }
      return groupIndex(a) - groupIndex(b) || statusOrder[a.status] - statusOrder[b.status] || a.name.localeCompare(b.name, "zh-CN");
    });
}

