function sharePayload(type, id) {
  if (type === "trip") {
    const trip = state.trips.find((item) => item.id === id);
    if (!trip) return null;
    return {
      type: "行程",
      name: trip.name,
      description: trip.status === "completed" ? trip.memory || tripCountriesText(trip) : `${tripCountriesText(trip)} · ${formatDateRange(trip.startDate, trip.endDate)}`,
      cover: trip.id,
    };
  }
  if (type === "country") {
    const place = state.worldPlaces.find((item) => item.code === id);
    if (!place) return null;
    const guide = getCountryGuide(id);
    return {
      type: "国家",
      name: place.name,
      description: guide ? `${guide.days} · ${guide.season}` : `${place.group}目的地，资料卡待补充。`,
      cover: id,
    };
  }
  if (type === "city") {
    const [countryCode, cityName] = id.split("|");
    const city = findWorldCity(countryCode, cityName);
    if (!city) return null;
    return {
      type: "城市",
      name: city.name,
      description: `${city.countryName} · ${normalizeGuideSentence(city.note, "适合收藏进下一趟旅行。")}`,
      cover: `${countryCode}-${city.name}`,
    };
  }
  if (type === "route") {
    const route = findRouteById(id);
    if (!route) return null;
    return {
      type: "路线",
      name: route.name,
      description: route.hint,
      cover: route.id,
    };
  }
  return null;
}

