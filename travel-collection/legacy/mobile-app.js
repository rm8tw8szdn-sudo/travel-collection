function cityRank(provinceCode, cityName) {
  const order = popularCityOrder[provinceCode] || provinceCities[provinceCode] || [];
  const normalized = cleanChinaCityName(cityName);
  const index = order.findIndex((name) => normalized.includes(name) || name.includes(normalized));
  return index === -1 ? order.length + 100 : index;
}

function getProvinceCities(place) {
  const cities = state.chinaCityMap[place.code] || (provinceCities[place.code] || []).map((name) => ({ code: cityKey(place.code, name), name, provinceCode: place.code }));
  return cities
    .map((city) => ({ ...city, code: cityCode(city), name: cleanChinaCityName(city.name), provinceCode: place.code }))
    .sort((a, b) => cityRank(place.code, a.name) - cityRank(place.code, b.name) || a.name.localeCompare(b.name, "zh-CN"));
}

