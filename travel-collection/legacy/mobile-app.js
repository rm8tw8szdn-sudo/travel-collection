function atlasMatches() {
  const query = state.search.trim().toLowerCase();
  const countries = state.worldPlaces
    .map((place) => ({ ...place, status: getStatus(place.code), guide: getCountryGuide(place.code) }))
    .filter((place) => {
      const isFavorite = state.favoriteCountries.has(place.code);
      if (state.atlasFilter === "visited" && place.status !== "visited") return false;
      if (state.atlasFilter === "blank" && place.status === "visited") return false;
      if (state.atlasFilter === "favorite" && !isFavorite) return false;
      if (!query) return true;
      const cityText = getWorldCitiesForCountry(place.code).map((city) => city.name).join(" ");
      return [place.name, place.code, place.group, ...(place.guide?.style || []), cityText].join(" ").toLowerCase().includes(query);
    })
    .sort((a, b) => {
      const aPriority = atlasPriorityCodes.indexOf(a.code);
      const bPriority = atlasPriorityCodes.indexOf(b.code);
      const aScore = aPriority === -1 ? 1000 : aPriority;
      const bScore = bPriority === -1 ? 1000 : bPriority;
      if (aScore !== bScore) return aScore - bScore;
      const statusScore = (place) => statusSets.world.visited.has(place.code) ? 0 : state.upcomingCountries.has(place.code) ? 1 : 2;
      return statusScore(a) - statusScore(b) || a.name.localeCompare(b.name, "zh-CN");
    });
  const cities = allWorldCities().filter((city) => {
    if (!query) return false;
    return [city.name, city.countryName, city.note, ...(city.spots || []).map((spot) => spot.name)].join(" ").toLowerCase().includes(query);
  });
  return { countries, cities };
}

