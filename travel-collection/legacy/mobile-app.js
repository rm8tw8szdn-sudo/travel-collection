function ensureEditableCountryGuide(code) {
  const place = state.worldPlaces.find((item) => item.code === code) || { code, name: code };
  if (!countryGuides[code]) {
    const placeholder = fallbackGuide(place);
    countryGuides[code] = {
      ...placeholder,
      status: "draft",
      routes: [],
      cities: [],
    };
  }
  const guide = countryGuides[code];
  guide.status = "draft";
  guide.style = Array.isArray(guide.style) && guide.style.length ? guide.style : ["自定义", "目的地预览"];
  guide.routes = Array.isArray(guide.routes) ? guide.routes : [];
  guide.cities = Array.isArray(guide.cities) ? guide.cities : [];
  return guide;
}

function addCityToCountryGuide(code, name, note) {
  const cityName = String(name || "").trim();
  if (!cityName) return false;
  const guide = ensureEditableCountryGuide(code);
  guide.cities.push({
    name: cityName,
    note: String(note || "").trim() || `${cityName} · 自定义补充城市。`,
    spots: [],
  });
  return true;
}

function addSpotToCountryCity(code, cityIndex, name, tag, tip) {
  const spotName = String(name || "").trim();
  if (!spotName) return false;
  const guide = ensureEditableCountryGuide(code);
  const city = guide.cities[Number(cityIndex)];
  if (!city) return false;
  city.spots = Array.isArray(city.spots) ? city.spots : [];
  city.spots.push({
    name: spotName,
    tag: String(tag || "").trim() || "自定义",
    tip: String(tip || "").trim() || "自定义补充的旅行收集点。",
  });
  return true;
}

