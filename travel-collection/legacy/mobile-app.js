  if (pickCountry) {
    event.preventDefault();
    event.stopPropagation();
    openAssignSheet(pickCountry.dataset.pickCountry);
    return;
  }

  const addRoute = event.target.closest("[data-add-route]");
  if (addRoute) {
    event.preventDefault();
    event.stopPropagation();
    const route = travelGroups.find((item) => item.id === addRoute.dataset.addRoute);
    route?.codes.forEach((code) => state.upcomingCountries.add(code));
    renderAll();
    return;
  }

  const routePageButton = event.target.closest("[data-route-page]");
  if (routePageButton) {
    event.preventDefault();
    event.stopPropagation();
    const query = state.search.trim().toLowerCase();
    const routeTotal = travelGroups.filter((route) => {
      if (!query) return true;
      return [route.name, route.hint, ...(route.keywords || [])].join(" ").toLowerCase().includes(query);
    }).length;
    const totalPages = Math.max(1, Math.ceil(routeTotal / ROUTES_PER_PAGE));
    if (routePageButton.dataset.routePage === "prev") state.routePage = Math.max(0, state.routePage - 1);
    if (routePageButton.dataset.routePage === "next") state.routePage = Math.min(totalPages - 1, state.routePage + 1);
    renderAll();
    document.querySelector(".collection-panel")?.scrollIntoView({ block: "start", behavior: "smooth" });
    return;
  }

  const provincePageButton = event.target.closest("[data-province-page]");
  if (provincePageButton) {
    event.preventDefault();
    event.stopPropagation();
    const totalPages = Math.max(1, Math.ceil(visiblePlaces().length / PROVINCES_PER_PAGE));
    if (provincePageButton.dataset.provincePage === "prev") state.provincePage = Math.max(0, state.provincePage - 1);
    if (provincePageButton.dataset.provincePage === "next") state.provincePage = Math.min(totalPages - 1, state.provincePage + 1);
    renderAll();
    document.querySelector(".collection-panel")?.scrollIntoView({ block: "start", behavior: "smooth" });
    return;
  }

  const toggle = event.target.closest("[data-toggle-visited]");
  if (toggle) {
    event.preventDefault();
    event.stopPropagation();
    const code = toggle.dataset.toggleVisited;
    setVisited(code, getStatus(code) !== "visited");
    return;
  }

  const cityToggle = event.target.closest("[data-city-toggle]");
  if (cityToggle) {
    event.preventDefault();
    event.stopPropagation();
    const provinceCode = cityToggle.dataset.cityToggle;
    const city = { code: cityToggle.dataset.cityCode, name: cityToggle.dataset.cityName, provinceCode };
    setCityVisited(provinceCode, city, !cityStatusSets.china.visited.has(cityCode(city)));
    return;
  }

  const cityUpcoming = event.target.closest("[data-city-upcoming]");
  if (cityUpcoming) {
    event.preventDefault();
    event.stopPropagation();
    const provinceCode = cityUpcoming.dataset.cityUpcoming;
    const city = { code: cityUpcoming.dataset.cityCode, name: cityUpcoming.dataset.cityName, provinceCode };
    setCityUpcoming(city, !state.upcomingCities.has(cityCode(city)));
    return;
  }

  const expand = event.target.closest("[data-expand-code]");
  if (expand && state.mode === "china") {
    const code = expand.dataset.expandCode;
    if (state.expandedProvinces.has(code)) {
      state.expandedProvinces.delete(code);
    } else {
      state.expandedProvinces.add(code);
    }
    renderAll();
    ensureProvinceCities(code);
    return;
  }

  const worldGroup = event.target.closest("[data-world-group]");
