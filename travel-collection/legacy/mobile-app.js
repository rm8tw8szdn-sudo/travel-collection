function setVisited(code, isVisited) {
  if (isVisited) {
    statusSets[state.mode].visited.add(code);
    if (state.mode === "world") state.manualVisitedCountries.add(code);
    if (state.mode === "world" && !state.visitedOrder.includes(code)) state.visitedOrder.push(code);
    if (state.mode === "world") state.upcomingCountries.delete(code);
  } else {
    statusSets[state.mode].visited.delete(code);
    if (state.mode === "world") state.manualVisitedCountries.delete(code);
    if (state.mode === "world") state.visitedOrder = state.visitedOrder.filter((item) => item !== code);
  }
  renderAll();
}

function setUpcomingCountry(code, isUpcoming) {
  if (isUpcoming) {
    state.upcomingCountries.add(code);
    if (!state.trips.some((trip) => trip.countries.includes(code))) {
      state.trips[0]?.countries.push(code);
    }
  } else {
    state.upcomingCountries.delete(code);
    state.trips.forEach((trip) => {
      trip.countries = trip.countries.filter((item) => item !== code);
    });
  }
  renderAll();
}

